public class Makan {
    
    public int makanKarbo(int porsi, int kaloriPerPorsi) {
        return porsi * kaloriPerPorsi;
    }

    public int makanProtein(int porsi, int kaloriPerPorsi) {
        return porsi * kaloriPerPorsi;
    }

    public int makanCemilan(int jumlah, int kaloriPerItem) {
        return jumlah * kaloriPerItem;
    }

}
